const mongoose = require('mongoose');

const userSchema = new mongoose.Schema({
  uid: {
    type: String,
    required: true,
    unique: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  name: {
    type: String,
    required: true
  },
  photoUrl: {
    type: String
  }
});

const userModel = mongoose.model('User', userSchema);

module.exports = userModel;
